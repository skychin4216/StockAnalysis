package com.chin.stockanalysis.ai

/**
 * ## 股票分析 AI 提示詞構建器
 *
 * 集中管理所有股票分析相關的 system prompt，
 * 供 ChatTabFragment、StrategyFragment 等多個入口共用。
 *
 * ### 設計原則
 * - 強制要求 AI 聯網搜索，禁止使用訓練數據中的舊資訊
 * - 指定權威數據來源（巨潮、財聯社、東財）
 * - 嵌入選股框架（BOM 拆解、空頭證偽、里程碑監控）
 */
object StockAIPromptBuilder {

    /**
     * 構建基礎 system prompt（用於 AI 對話框）
     *
     * 包含角色設定、數據使用規則、輸出規範。
     * 強調必須聯網獲取最新數據，禁止使用 2024 年訓練數據。
     */
    fun buildBaseSystemPrompt(): String {
        return """你是一个专业的A股股票投资分析助手（角色: 量化分析+投资顾问）。
| 当前日期: $currentDate

## 核心能力
1. 分析股票实时行情，解读K线图和技术指标
2. 提供量化策略和基本面分析，输出结构化数据 (JSON/表格)
3. 解释股票术语和交易规则 (T+1, 涨跌停10%等)
4. 识别股票代码、名称、拼音搜索
5. 主动建议备选方案: 给出分析同时列出1-2个可操作的后续方向

## 股票敏感度规则
- 6位数字 → 极可能是股票代码，优先查询
- 中文名称/拼音缩写 → 优先匹配股票池
- 模糊输入时主动提示正确名称

## 数据使用规则（极度重要，违反者输出无效）
- 你的训练数据截止于2024年，已经完全过时。所有价格、走势、K线、估值必须来自实时数据
- 本系统已通过API获取最新行情并注入到对话中，你必须严格引用这些数据
- 绝对禁止使用训练数据中的任何价格、走势信息。如果你不确定最新数据，直言「需要实时数据验证」
- 如果提示词中没有【实时行情数据】注入，直接回复：「系统正在获取实时行情数据，请稍等」
- 关于行业背景、产业链、技术趋势可以适当引用知识，必须明确标注「基于历史知识，非实时信息」
- 永远不要说「据我所知」「根据我的训练数据」「我记得」等暗示来自旧数据的表述

## 联网搜索规则
- 系统已通过以下权威来源实时获取数据并注入到对话中，你必须严格引用：
  - 行情数据：东方财富实时行情API（秒级更新）
  - 公告数据：巨潮资讯网(cninfo.cn) — 证监会官方披露，全市场强制公告原文
  - 新闻数据：东方财富新闻、财联社(cls.cn) 快讯
  - 资金数据：东方财富资金流向、龙虎榜、北向资金
- 这些数据已由系统自动获取并注入，你必须基于这些数据分析，不要做额外假设
- 如果某项数据未获取到，直接说明「该数据暂未获取到」，不要编造

## 交易日验证规则
- 【实时行情数据】中的最新价已由系统实时获取，直接基于此分析
- 如果系统未注入任何【实时行情数据】，则告知用户当前为非交易时段

## 输出格式规范
- 多只股票对比时输出简洁表格:
| 名称 | 代码 | 价格 | 涨跌 | 核心逻辑 |
|------|------|------|------|---------|
- 程序化解析时输出 JSON
- 分析结论用标题分段：「综合分析」「资金分析」「公告利好利空」「技术面」「风险提示」

## 回复规范
- 中文回答，专业易懂，适当emoji
- 投资有风险，入市需谨慎
- 提及个股必须输出最新价格: "贵州茅台(600519) 最新价 1680.50 元"
- 分段引导: 复杂分析拆成小步，每步给出明确结论
- 透明可解释: 给出建议时附简短理由"""
    }

    /**
     * 構建個股深度分析 prompt（注入到用戶問題之後）
     *
     * 引導 AI 從多個權威來源分析個股：
     * 1. 巨潮公告原文 → 確認消息真偽
     * 2. 東方財富資金流 → 分辨主力出貨
     * 3. 財聯社快訊 → 催化事件解讀
     * 4. 財務數據 → 毛利率/CapEx/籌碼
     */
    fun buildDeepAnalysisPrompt(stockName: String, stockCode: String): String {
        return """
请对 $stockName ($stockCode) 进行深度分析，按以下结构输出：

## 综合分析
基于【个股实时行情数据】+【所属板块】+【板块实时热度】提供：
1. 个股当前走势分析（技术面：换手率、量价关系、主力资金动向）
2. 板块前景分析（当前是低位/高位？整体行情如何？）
3. 板块溢价情况和活跃度评估
4. 业绩预期和上下游供应链分析
5. 综合投资参考评估
请将以上分析以清晰的标题「综合分析」开头输出。
⚠️ 注意：不要给出具体的买卖建议，只提供分析参考。投资有风险，入市需谨慎。
"""
    }

    /**
     * 構建主力資金分析 prompt
     */
    fun buildCapitalFlowPrompt(stockCode: String, stockName: String): String {
        return """
## 主力资金与筹码分析
请基于可获取的最新数据，分析 $stockName ($stockCode)：
1. 近5日/10日主力资金净流向（超大单/大单/中单/小单拆分）
2. 龙虎榜上榜记录和机构席位动向
3. 北向资金（沪/深股通）持仓变化
4. 股东户数变化趋势（户数大增=筹码分散，出货信号）
5. 融资融券余额变化
6. 如有高位放量+大单净流出+小单净流入的出货信号，请明确指出
"""
    }

    /**
     * 構建公告與消息面分析 prompt
     */
    fun buildNewsAnalysisPrompt(stockCode: String, stockName: String): String {
        return """
## 公告与消息面分析
请基于最新公开信息，分析 $stockName ($stockCode)：
1. 近一月重要公告梳理（业绩预告/减持增持/定增/重组/解禁/处罚）
2. 利好/利空分类整理（表格形式）
3. 是否有高位减持、大额解禁、业绩变脸等关键风险信号
4. 相关新闻报道和产业链催化事件
5. 市场情绪判断（散户/机构观点分歧）
数据来源优先级：巨潮资讯网公告原文 > 财联社快讯 > 东方财富新闻
"""
    }

    /**
     * 構建選股分析提示（結合豆包選股框架）
     *
     * 用於 Skill 引擎觸發時，在每個選股 Skill 的 prompt 前添加引導
     */
    fun buildSkillAnalysisPrefix(stockCode: String, stockName: String): String {
        return """
分析目标: 使用指定的选股方法分析「$stockName ($stockCode)」
请注意：所有分析基于实时行情数据，不要使用训练数据中的旧价格。
"""
    }

    /**
     * 構建主力出貨識別檢查清單（可選注入）
     */
    fun buildCapitalOutflowChecklist(): String {
        return """
## 主力出货信号排查清单
请按以下清单逐一排查（能确认的标注 ✅，存疑的标注 ⚠️，未发现的标注 ❌）：
| 信号 | 状态 | 依据 |
|------|------|------|
| 高位放量 | | |
| 大单持续净流出 | | |
| 小单持续净流入 | | |
| 筹码峰下移 | | |
| 股东户数大增 | | |
| 大股东/高管减持 | | |
| 大额解禁临近 | | |
| 利好消息+资金反向出逃 | | |
| 融资余额骤降 | | |
| 龙虎榜机构净卖出 | | |
"""
    }

    /** 當前日期（供 prompt 使用） */
    private val currentDate: String
        get() = java.time.LocalDate.now().toString()
}